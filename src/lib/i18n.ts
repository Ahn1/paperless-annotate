const dictionaries: Record<Lang, Record<TranslationKey, string>> = { de, en }

interface I18nState {
  lang: Lang
  setLang: (lang: Lang) => void
}

export const useI18nStore = create<I18nState>()(
  persist(
    (set) => ({
      lang: navigator.language.startsWith('de') ? 'de' : 'en',
      setLang: (lang) => set({ lang }),
    }),
    { name: 'pa-lang' },
  ),
)

export function translate(lang: Lang, key: TranslationKey, params?: Record<string, string | number>): string {
  let text: string = dictionaries[lang][key] ?? key
  if (params) {
    for (const [k, v] of Object.entries(params)) text = text.replaceAll(`{${k}}`, String(v))
  }
  return text
}

/** Hook: `const t = useT()` → `t('documents.title')` */
export function useT() {
  const lang = useI18nStore((s) => s.lang)
  return (key: TranslationKey, params?: Record<string, string | number>) => translate(lang, key, params)
}

export function useLocale(): string {
  return useI18nStore((s) => s.lang) === 'de' ? 'de-DE' : 'en-US'
}
